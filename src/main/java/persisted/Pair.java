package persisted;

import javax.persistence.Embeddable;
import java.io.Serializable;

@Embeddable
public class Pair {

    private String key;

    private String value;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
